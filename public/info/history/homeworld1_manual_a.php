### 發現卡爾－托巴（Khar-Toba）

在 1106 年，我們發射了一顆強力的雷達衛星，意圖在星系中的其他地方找到更大的碎片帶。由於姿勢調整引擎的故障，讓這個衛星轉而面對卡拉克，並且開始掃描的動作。計畫中的一名技術員雷凱博·加拉席（Leykab Jaraci）發現了一個出乎意料的巨大反射源。分析顯示出這個強力的雷達穿透了赤道的沙漠達 75 公尺之多，有強烈的證據顯示，該處有一座環繞著巨大金屬結構的城市。

在 1110 年，重複的雷達掃瞄證據終於說服了科技官僚們將所有的資源專注在前往大沙漠的太險上。雖然他們遇上了讓穿著最現代化的太空衣的人也會膽寒的狀況，但這些冒險的先驅們依舊找到了稍後被稱為「初始之城」的卡爾－托巴[^A5]。光是這樣的發現就足以成為考古學家的夢想，但是當正中央的金屬結構被證實為一艘先進船艦的股價時，更是掀起了全球的熱潮。除了一連串的金屬骨架之外，這艘船艦幾乎沒有剩下什麼東西。但真正的寶藏就在一個經過層層防護，深埋在地底下的房間之中。1112 年的第一次冒險中，在沿著古老的電纜探索時，工程師們打開了一個經過特殊防護，保存有該艘船艦能量裝置的房間。幾經艱辛之後，這個裝置才被運送到極地的首都特爾城。在經過逆向工程學的研究之後，這個裝置讓我們的能源和材料科學又往前躍進了一大步。但是，真正讓我們的科技超前五百年的，是連在這個裝置上的固態超空間傳導系統。在數十年的研究和分析之後，我們已經準備好朝著銀河跨出第一步。但是，直到 1112 年時，我們才知道還有多遠的路要走。

反應爐和超空間系統被視作卡爾－托巴的寶石，在他們被移往兩極的實驗室之中後，這座古老的城市留給了少數幾名專注的考古學家。他們在卡拉克最艱苦的環境中掙扎著繼續進行研究。這群學者由名為美維斯・沙加德（Mevath Sagald）的年輕女子所率領。在他鍥而不捨的努力之下，終於挖掘出了卡爾－托巴秘密的天文神廟，賜予我們整個文明新的目標與意義。一場意外讓他幾乎單槍匹馬的進行這次的發掘，當他打開神廟的內殿之後，他立即看出了眼前那顆鐫刻黑石的重要性。

<div class="embedimg" style="width:145px;"><img src="/i/manual/hw1/fig_1_3.png"><p>圖 1.3: 指引之石在卡爾－托巴的廢墟中被發現，年代至少可以追朔至三千年前。精確的銀河系圖樣和卡拉克的座標刻在黑色的表面上，在一個靠近銀河中心的小小區域上刻著一個古老的字：家。</p></div>
考古學家沙加德找到的東西對一般人來說十分不起眼，但卻是我們未來的一個重要里程碑。當他進一步研究這顆石頭時，發現這顆雕工精細的石頭險些在高熱下徹底的被毀。不論他原先是用做什麼目的，現在都已經難以辨認。古人將它保存起來，似乎是留給後代的訊息。它的表面上刻著一個代表我們銀河的簡單示意圖，以及一條從旋臂邊緣通往銀河中心深處的直線。在靠近卡拉克位置的點旁邊有一串顯示出三度空間向量的座標。在線的另一邊則只有一個字，雖然古老，卻是所有氏族通用的語言：西亞加拉...家。

這樣簡單，現在被稱為指引之石的物品，對我們文化所產生的衝擊幾乎無法衡量。我們的材料科學家確認了這樣物品的年紀至少超過三千年，而且他們也確信如果能夠前往這樣物品所產自的星系，必定可以立刻比對出來。在漫長的求生和內戰的歷史之後，卡拉克並非是我們真正家園的鐵證帶來了一個空前齊心合力的合作時代[^A6]。

在過去的兩百五十年之中，沒有任何重大的衝突和流血事件。我們把所有工業和科學的資源全部投注在一個目的上：

回到西亞加拉，我們的母星。

[^A4]: 並非所有的居民都贊同太空探險的計畫。較為貧窮的氏族中有極大部分的民眾認為科技和工業的產能不該浪費在這種計畫上。他們認為這些資源應該用來試著改善卡拉克，或研究可以養育更多人口的方法，並阻擋步步逼近的沙漠。隨著第一次軌道飛行的發射日期漸漸逼近，名為波・東印的神學家開始宣揚早已為人遺忘的古老傳說。傳說中如果我輩再一次地做出無知的舉動，將會徹底的觸怒上帝，帶來無比的悲劇。這提供了讓反對勢力集結起來的力量，並在南北極都造成了相當大的震撼。最後，狂熱的暴徒們試著在發射前夕破壞西魯明計畫的太空船。這個悲劇在兼任科技專家的政治領袖睿智之下方才化解。他們以肉身阻擋基地的砲塔不要對民眾開火，在發射所需的十四個小時之中不停地說服民眾。波・東印溜進防衛嚴密的基地之中，在火箭的主引擎之下不斷的祈求救贖，直到最後高溫的烈焰將他蒸發為止。他是這場暴動中唯一的烈士。

[^A5]: 卡爾－托巴似乎是我們的祖先在降落於卡拉克之後所建造的第一個城市。針對這艘古老船艦的分析顯示，他在降落的時候受到了巨大的創傷，無法將成員移動到氣候較為溫和的極地去。因此，整座城市環繞著這艘船艦建造，並且大多數延伸到地底，想必是我們的祖先為了逃避沙漠中的高溫和沙暴所做的權宜措施。最靠近船隻的許多建築都是用他的殘骸所建成的。隨著城市往外擴展，許多的建築物變成由手在當地沙岩上所刻出來的簡陋房屋。巨大的核融合爐心被從船隻中移動到地底來來提供所有居民需要的能源。在核融合爐失效之後，這座城市應該很快就崩潰了。

[^A6]: 如果考古學家沙加德把指引之時帶回了他的氏族，那麼卡拉克必定會陷入戰火連天的爭奪中，每個氏族都會爭相取得研究的權利。不過，沙加德發現他手中物品的重要性遠遠超過了對氏族或是對家族的忠誠。在一次各氏族族長齊聚的會議中，一位滿身是沙的女子帶著一塊古老的石頭打斷了他們激烈的爭辯。在第二天的夜晚，當地的科學家不只證實了這樣物品的真實性，更公開了它背後的無窮可能性。在獲知了這項事實之後，所有的族長立刻啟程趕回族中，宣佈我們並非是此地的原住民。從最大的工業氏族到最小的農業氏族，不約而同地得出了一個決議。全星球的工業和科技的力量必須投入跟隨指引之石路線的這項任務中。在我們來到這顆星球之後有史以來第一次，所有的人都成了同一個氏族。


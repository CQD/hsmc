### 起源之謎

<div class="embedimg" style="width:185px;"><img src="/i/manual/hw1/fig_1_1.png"><p>圖 1.1: 從太空中鳥瞰沙漠行星卡拉克</p></div>
有關於我們來自於何方這個問題，在有歷史紀錄的一千三百多年中一直是爭論不休的話題。由於卡拉克星[^A1]極為艱困的環境，導致神話和傳說中一傳頌著一些不需掙扎求生的美好天堂。在不久之前，有關於我們遙遠過去的臆測皆屬於宗教的範疇[^A2]；但是，在「理性萌芽時代」[^A3]開始之後，因為生物學和化學的蓬勃發展，學者們才發現了我們的生化組成和卡拉克星上的生命大異其趣，幾乎沒有任何的共通點。極為諷刺的是，也正是因為這場科學革命所帶來的許多科學進展，才提供了適當的哲學環境，讓最新的理論和最古老的神話結合再一起，誕生了目前為一般人所接受的「外星創世論」。除了極少數的細菌和唯一的一種草食動物之外，我們的蛋白質螺旋和卡拉克星上所有的原住生物完全不同。這讓我們只能別無選擇的開始考慮自己是否為外來的生命。顯而易見的，這個問題的答案只會引來更多的問題。

[^A1]: 卡拉克是個十分古老的行星（大約六十七億年），所有的地質活動幾乎都已經到了末期。大多數的地形起伏都受到風化作用的影響而消磨殆盡，除了極地區域之外，整座行星被廣闊的大沙漠所包圍。這些極地之所以沒有被沙漠吞沒，都必須歸功於北極的三個海以及南極的馬哲理安洋。在赤道地區的溫度可以高達水的沸點。除了微生物之外，唯一能夠在該處存活的生命必須躲在深層的地底或是地下的水道中。也有少部分的生物是透過在最高溫的季節進入休眠狀態來保持生命的。極區可說是此地最適合我們同胞居住的地方，但由於罕見的可耕地和稀少的資源，讓全球的人口無法超過三億人。

[^A2]: 卡拉克的早期歷史充滿了各氏族為了領土和宗教信仰而起的爭端。這樣的衝突在 520 年的時候到達了高潮，當時北極兩個最強大的氏族，席德（Siiddim）和加爾西安（Gaalsien）為了「上帝為何將我們遺棄在這樣的地方」而宣戰。席德相信我們本來是一個居住在天堂中的偉大民族，由於我們的驕傲和自大，於是上帝將我們貶到這樣的世界中。而加爾西安認為這種想法對他們的信仰是一大褻瀆。加爾西安認為我們被創造的目的就是不斷的受苦，卡拉克就是我們唯一的家園。這場戰爭甚至也蔓延到了南極區域。甚至更細微的認知差異也導致了接下來一連串大大小小的戰爭，延續了將近三百年之久。也造成了許多事足之間的血海深仇。直到指引之石的出現才化解了這些仇怨。

[^A3]: 異端戰爭讓我們的同胞淪落到滅種的邊緣，珍貴的資源和社會架構在三百年之久的宗教衝突中遭到無情的摧毀。在 810 年時，當所有的派系資源耗盡，紛紛陷入內部衝突時，一個居住在特爾（Tiir）的小氏族放棄了隱居生活，重新入世。這個不為人知的北方氏族是第一個發展出化學炸藥的勢力，也因此在戰爭中幾乎無人能敵。他們同時也傳播一種以科學和邏輯為基礎的世界觀，並對那些願意居住在這種世界中的人們提供庇護。幾場決定性的會戰讓所有的人明白，其他迷信的氏族根本毫無擊敗那百（Nabal）氏族的機會。在二十年之內，特爾就成為新的首都，理性萌芽時代就此展開。


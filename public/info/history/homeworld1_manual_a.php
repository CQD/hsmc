### 外星創世論的疑問，以及早期的太空飛行任務

<div class="embedimg" style="width:175px;float:left;"><img src="/i/manual/hw1/fig_1_2.png"><p>圖 1.2: 我們所在的螺旋銀河中心的照片。水平的暗帶代表的是被大量的宇宙塵所佔據的空間。相較於距離三萬五千光年外的明亮銀河中心，自然顯得相當的黯淡。</p></div>
在種種資料和研究的累積之後，很明顯的，我們這個種族應該是卡拉克的新住民。不過，這個理論並沒有讓我們滿足。我們用來抵達此地的工具和原因依舊成為激烈爭辯的中心，甚至促使了第一次軌道飛行任務的執行[^A4]。軌道探險時代的來臨帶回了我族並非誕生於卡拉克星的證據。在我們進展到載人飛行之後，太空人們立刻就在高層軌道中發現了不尋常的金屬碎片。緊接著的搜尋任務有了更驚人的成果。雖然我們只能找到手掌大的碎片，但樣本很快地就被送回大氣層內，降落在無垠的沙漠中。一開始的分析證明了這些是經過高科技加工的工具碎片。深入的原子分析證實了成分中擁有卡拉克缺少的痕量元素和同位素（稍後更證明了，整個星系中都沒有類似的物質）。

這是我們來自何處謎題的另外一個碎片，但也只能證明曾經有某種外星的裝置或是船艦曾經環繞過我們的星球，並無法帶來任何進一步的推論。這些微小的碎片為冶金學和製造學帶來了革命性的進展，讓我們了解到高張力的複合物是可以製造出來的。這也讓我們的推進動力學獲得了大幅的進展。我們一開始只是使用不穩定的重元素進行核裂變，接著是利用隨處皆可取用的氫設計核融合裝置；同時慢慢的將輻射屏蔽系統改良，變得更輕更小。這些科技的結合更鼓舞了我們的太空任務，讓我們的步伐成了跳躍。我們已經站在星系的邊緣，準備一窺宇宙的堂奧；但命運的作弄又讓我們把目光轉回到我們所居住的星球上。

